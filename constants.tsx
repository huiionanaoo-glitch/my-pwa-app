export const HEXAGRAMS: Hexagram[] = HEXA_DATA.map((data, index) => {
  const [name, upper, lower] = data;
  const upperBin = TRIGRAMS[upper].binary;
  const lowerBin = TRIGRAMS[lower].binary;
  // binary 字符串：index 0 为初爻，index 5 为上爻
  const binary = lowerBin + upperBin; 
  
  return {
    id: index + 1,
    name,
    binary,
    meaning: `${upper}${lower}${name}`,
    upperTrigram: upper,
    lowerTrigram: lower,
    guaci: GUACI_TEMPLATES[name] || "卦理深藏，待君悟之。",
    tuanzhuan: `象曰：${upper}${lower}，${name}。君子以应天顺人。`,
    sequence: index + 1,
    palace: PALACE_NAMES[Math.floor(index / 8)],
    nature: "分宫次序"
  };
});
